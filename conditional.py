def hitung(angka1, aritmatika, angka2):
    if aritmatika == "+":
        print (angka1 + angka2)
        
    elif aritmatika == "-":
        print (angka1 - angka2)

    elif aritmatika == "*":
        print (angka1 * angka2)

    elif aritmatika == "/":
        print (angka1 / angka2)
           
    else :
        print ("nilai yang diinput salah")
    
hitung(5, "^", 9)